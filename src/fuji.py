    def __init__(self, servoMotors):
        self.servoMotors = servoMotors

    def drawing(func):
        """
            drawing a part of areas
            kwargs
             timing         [int]           ... timing for adding color            (default: 3 dot)
             big_time       [int]           ... sleep time for adding color        (default: 3 sec)
             small_time     [int]           ... sleep time for moving next pixel   (default: 0.7 sec)
             noise_range    [int]           ... servoMotors[3] noise ranging, ±x   (default: 3)
        """

        def wrapper(self, *args, **kwargs):
            track_lst = func(self, *args, **kwargs)
            count_of_pixels = 0
            for track_path in track_lst:
                servo00 = track_path[0]
                servo01 = track_path[1]
                servo02 = track_path[2]
                servo03 = track_path[3]

                if count_of_pixels % kwargs["timing"] == 0:
                    self.servoMotors[0].setAngle(servo00)
                    self.servoMotors[1].setAngle(servo01-5)
                    self.servoMotors[2].setAngle(servo02-5)
                    self.servoMotors[3].setAngle(servo03)
                    _ = input()

                    sleep(kwargs["big_time"])
                noise = randint(-kwargs["noise_range"], kwargs["noise_range"])
                servo03 += noise
                print(f"{servo00}, {servo01}, {servo02}, {servo03}")
                self.servoMotors[0].setAngle(servo00)
                self.servoMotors[1].setAngle(servo01)
                self.servoMotors[2].setAngle(servo02)
                self.servoMotors[3].setAngle(servo03)
                sleep(0.3)
                self.servoMotors[0].setAngle(servo00)
                self.servoMotors[1].setAngle(servo01)
                self.servoMotors[2].setAngle(servo02-5)
                self.servoMotors[3].setAngle(servo03)
                sleep(kwargs["small_time"])
                count_of_pixels += 1
        return wrapper

